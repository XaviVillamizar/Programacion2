    private void btnRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaizActionPerformed
        // TODO add your handling code here:
    contenido = txtOperacion.getText();
    if (contenido.length() > 0) {
        double resultadoNumerico = Math.sqrt(Double.parseDouble(contenido));
    DecimalFormat df = new DecimalFormat("#.##"); 
    String resultadoFormateado = df.format(resultadoNumerico);
        txtOperacion.setText("√" + contenido);
        txtResultado.setText(resultadoFormateado);
        txtOperacion.setText(txtResultado.getText());
}

    }//GEN-LAST:event_btnRaizActionPerformed

