    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
        String[] partesOperacion = txtOperacion.getText().split("\\s+");
        if (partesOperacion.length == 3) {
            valor2 = partesOperacion[2];
            if(valor2.equals("0") && operacion.equals("/")){
                txtResultado.setText("ERROR");
            }else{
                String resultado = operaciones.calcular(valor1, valor2, operacion);
                txtResultado.setText(resultado);
                txtOperacion.setText(txtResultado.getText());
            }
        }
    }//GEN-LAST:event_btnIgualActionPerformed

